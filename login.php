<?php
// login.php
require_once 'config.php';

$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $email = $_POST['email'] ?? '';
    $password = $_POST['password'] ?? '';
    
    $conn = getConnection();
    $stmt = $conn->prepare("SELECT id_user, email_user, password_user, role_user, id_pegawai_user FROM users WHERE email_user = ?");
    $stmt->bind_param("s", $email);
    $stmt->execute();
    $result = $stmt->get_result();
    
    if ($result->num_rows > 0) {
        $user = $result->fetch_assoc();
        if (password_verify($password, $user['password_user'])) {
            $_SESSION['user_id'] = $user['id_user'];
            $_SESSION['email'] = $user['email_user'];
            $_SESSION['role'] = $user['role_user'];
            $_SESSION['id_pegawai'] = $user['id_pegawai_user'];
            
            header('Location: index.php');
            exit();
        } else {
            $error = 'Email atau password salah!';
        }
    } else {
        $error = 'Email atau password salah!';
    }
    
    $stmt->close();
    $conn->close();
}
